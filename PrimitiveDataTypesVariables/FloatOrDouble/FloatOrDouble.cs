using System;

class FloatOrDouble
{
    static void Main()
    {
        float a = 12.345F;
        float b = 3456.091F;
        double c = 34.567839023D;
        double d = 8923.1234857D;
        Console.WriteLine("float:{0} ", a);
        Console.WriteLine("float:{0} ", b);
        Console.WriteLine("double:{0} ", c);
        Console.WriteLine("double:{0} ", d);
    }
}