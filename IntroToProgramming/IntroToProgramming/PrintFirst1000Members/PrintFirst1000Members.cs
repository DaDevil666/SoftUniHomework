using System;

class First1000Members
{
    static void Main()
    {
        Console.WriteLine("For 2");
        for (var i = 2; i < 1003; i++)
        {
            Console.WriteLine(i);
        }
        Console.WriteLine("For -3");
        for (var i = -3; i > -1004; i--)
        {
            Console.WriteLine(i);
        }
        Console.WriteLine("For 4");
        for (var i = 4; i < 1005; i++)
        {
            Console.WriteLine(i);
        }
        Console.WriteLine("For -5");
        for (var i = -5; i > -1006; i--)
        {
            Console.WriteLine(i);
        }
        Console.WriteLine("For 6");
        for (var i = 6; i < 1007; i++)
        {
            Console.WriteLine(i);
        }
        Console.WriteLine("For -7");
        for (var i = -7; i > -1008; i--)
        {
            Console.WriteLine(i);
        }
    }
}
